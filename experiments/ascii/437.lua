function getCp437Index(char)
    for i = 0, 255 do
        if cp437_chars[i] == char then
            return i
        end
    end
    return nil -- Character not found
end

function get437CharCodes(text)
    local result = {}
    for p, c in utf8.codes(text) do
        local char = utf8.char(c)
        local index = getCp437Index(char)
        if index then
            table.insert(result, index)
            --print("Character:", char, "CP437 index:", index)
        else
            table.insert(result, nil)
            --print("Character:", char, "not found in CP437 set")
        end
    end
    return result
end
