#!/bin/bash
# Build a web version of a Playtime scene
# Usage: ./build-web.sh [scene-name]
# Example: ./build-web.sh catapult

set -e

SCENE="${1:-catapult}"
PLAYTIME_DIR="$(cd "$(dirname "$0")/.." && pwd)"
BUILD_DIR="/tmp/playtime-web-build"
OUTPUT_DIR="$(dirname "$0")/output-${SCENE}"

echo "=== Building web version of scene: $SCENE ==="

# Clean
rm -rf "$BUILD_DIR"
mkdir -p "$BUILD_DIR"

# Copy source files (skip dev-only stuff)
echo "Copying files..."
cp -r "$PLAYTIME_DIR/src" "$BUILD_DIR/src"
cp -r "$PLAYTIME_DIR/vendor" "$BUILD_DIR/vendor"
cp -r "$PLAYTIME_DIR/assets" "$BUILD_DIR/assets"
cp "$PLAYTIME_DIR/conf.lua" "$BUILD_DIR/conf.lua"

# Copy scene files
mkdir -p "$BUILD_DIR/scripts"
cp "$PLAYTIME_DIR/scripts/${SCENE}.playtime.json" "$BUILD_DIR/scripts/"
if [ -f "$PLAYTIME_DIR/scripts/${SCENE}.playtime.lua" ]; then
    cp "$PLAYTIME_DIR/scripts/${SCENE}.playtime.lua" "$BUILD_DIR/scripts/"
fi

# Copy textures if they exist and scene references them
if [ -d "$PLAYTIME_DIR/textures" ]; then
    cp -r "$PLAYTIME_DIR/textures" "$BUILD_DIR/textures"
fi

# Create web-compatible main.lua
cat > "$BUILD_DIR/main.lua" << 'MAINLUA'
-- Web build of Playtime scene
-- Auto-generated by build-web.sh

local SCENE_NAME = "SCENE_PLACEHOLDER"

-- Skip dev tools that don't work in browser
local IS_WEB = true

local prof = { push = function() end, pop = function() end, write = function() end }
local manual_gc = function() end

local blob = require 'vendor.loveblobs'
local recorder = require 'src.recorder'
local ui = require('src.ui.all')
local playtimeui = require 'src.playtime-ui'

local selectrect = require 'src.selection-rect'
local script = require 'src.script'
local objectManager = require('src.object-manager')

local utils = require 'src.utils'
local box2dDraw = require 'src.physics.box2d-draw'
local box2dDrawTextured = require 'src.physics.box2d-draw-textured'
local box2dPointerJoints = require 'src.physics.box2d-pointerjoints'
local camera = require 'src.camera'
local cam = camera.getInstance()

local snap = require 'src.physics.snap'
local keep_angle = require 'src.keep-angle'

local InputManager = require 'src.input-manager'
local state = require 'src.state'
local editorRenderer = require 'src.editor-render'

local sceneIO = require 'src.io'
local scriptMod = require 'src.script'

local function loadSceneFromFilesystem(sceneName)
    local jsonPath = 'scripts/' .. sceneName .. '.playtime.json'
    local luaPath = 'scripts/' .. sceneName .. '.playtime.lua'

    local jsonData = love.filesystem.read(jsonPath)
    if not jsonData then
        error("Scene not found: " .. jsonPath)
    end

    state.selection.selectedJoint = nil
    state.selection.selectedObj = nil
    sceneIO.load(jsonData, state.physicsWorld, cam)

    local luaData = love.filesystem.read(luaPath)
    if luaData then
        state.scene.sceneScript = scriptMod.loadScript(luaData, luaPath)()
        scriptMod.setEnv({ worldState = state.world, world = state.physicsWorld, state = state })
        scriptMod.call('onStart')
    end
end

function love.load()
    local fontHeight = 25
    local font = love.graphics.newFont('assets/Arial Narrow.ttf', fontHeight)
    love.keyboard.setKeyRepeat(true)
    love.graphics.setFont(font)
    ui.init(font, fontHeight)

    love.physics.setMeter(state.world.meter)
    state.physicsWorld = love.physics.newWorld(0, state.world.gravity * love.physics.getMeter(), true)

    local w, h = love.graphics.getDimensions()
    camera.setCameraViewport(cam, w, h)
    camera.centerCameraOnPosition(325, 325, 2000, 2000)

    loadSceneFromFilesystem(SCENE_NAME)
end

local _physicsCallbacks = require('src.physics.physics-callbacks')

function love.update(dt)
    local scaled_dt = dt * state.world.speedMultiplier
    if not state.world.paused then
        if state.world.playWithSoftbodies then
            for _, v in ipairs(state.world.softbodies) do
                v:update(scaled_dt)
            end
        end
        local velocityiterations = 8
        local positioniterations = 3
        local substeps = 1
        local step = scaled_dt / substeps
        for _ = 1, substeps do
            state.physicsWorld:update(step, velocityiterations * 2, positioniterations * 2)
        end

        script.call('update', scaled_dt)
        snap.update(scaled_dt)
        local interacted = box2dPointerJoints.getInteractedWithPointer()
        local newHitted = utils.map(interacted, function(h)
            local ud = (h:getUserData())
            local thing = ud and ud.thing
            return thing
        end)
        keep_angle.update(scaled_dt, newHitted)
    end

    box2dPointerJoints.handlePointerUpdate(scaled_dt, cam)
    if state.interaction.draggingObj then
        InputManager.handleDraggingObj()
    end
end

function love.draw()
    local w, h = love.graphics.getDimensions()

    if state.world.darkMode then
        love.graphics.clear(.1, 0.2, .1)
    else
        love.graphics.clear(245/255, 245/255, 220/255)
    end

    if state.editorPreferences.showGrid then
        editorRenderer.drawGrid(state.world.darkMode and {1,1,1,.1} or {0,0,1,.1})
    end

    box2dDrawTextured.makeCombinedImages()
    cam:push()
    love.graphics.setColor(1, 1, 1, 1)

    box2dDraw.drawWorld(state.physicsWorld, state.world.debugDrawMode)
    if state.world.showTextures then
        box2dDrawTextured.drawTexturedWorld(state.physicsWorld)
    end
    script.call('draw')

    editorRenderer.renderActiveEditorThings()
    cam:pop()

    if state.interaction.startSelection then
        selectrect.draw(state.interaction.startSelection)
    end

    playtimeui.drawUI()
    script.call('drawUI')

    if state.scene.sceneScript and state.scene.sceneScript.foundError then
        love.graphics.setColor(1, 0, 0)
        love.graphics.print(state.scene.sceneScript.foundError, 0, h / 2)
        love.graphics.setColor(1, 1, 1)
    end

    love.graphics.setColor(1, 1, 1)
    love.graphics.print(string.format("%03d", love.timer.getFPS()), w - 80, 10)
end

function love.wheelmoved(dx, dy)
    if not ui.overPanel then
        local newScale = cam.scale * (1 + dy / 10)
        if newScale > 0.01 and newScale < 50 then
            cam:scaleToPoint(1 + dy / 10)
        end
    end
    ui.mouseWheelDx = dx
    ui.mouseWheelDy = dy
end

function love.keypressed(key)
    ui.handleKeyPress(key)
    InputManager.handleKeyPressed(key)
    script.call('onKeyPress', key)
end

function love.mousemoved(x, y, dx, dy)
    InputManager.handleMouseMoved(x, y, dx, dy)
end

function love.mousepressed(x, y, button, istouch)
    InputManager.handleMousePressed(x, y, button, istouch)
end

function love.touchpressed(id, x, y, dx, dy, pressure)
    InputManager.handleTouchPressed(id, x, y, dx, dy, pressure)
end

function love.mousereleased(x, y, button, istouch)
    InputManager.handleMouseReleased(x, y, button, istouch)
end

function love.touchreleased(id, x, y, dx, dy, pressure)
    InputManager.handleTouchReleased(id, x, y, dx, dy, pressure)
end

function love.textinput(t)
    ui.handleTextInput(t)
end

function love.resize() end
function love.quit() end
MAINLUA

# Patch scene name into main.lua
sed -i '' "s/SCENE_PLACEHOLDER/${SCENE}/" "$BUILD_DIR/main.lua"

# Stub out vendor modules that don't work in browser
cat > "$BUILD_DIR/vendor/claude-bridge.lua" << 'EOF'
return { update = function() end, recordError = function() end }
EOF

cat > "$BUILD_DIR/vendor/lurker.lua" << 'EOF'
return { update = function() end, onerror = function() end }
EOF

cat > "$BUILD_DIR/vendor/jprof.lua" << 'EOF'
return { push = function() end, pop = function() end, write = function() end }
EOF

cat > "$BUILD_DIR/vendor/ProFi.lua" << 'EOF'
return {}
EOF

# Stub peeker if it exists
if [ -f "$BUILD_DIR/vendor/peeker.lua" ]; then
    cat > "$BUILD_DIR/vendor/peeker.lua" << 'EOF'
return { update = function() end, attach = function() end, detach = function() end }
EOF
fi

# Remove spec/test files to reduce size
rm -rf "$BUILD_DIR/spec" "$BUILD_DIR/claudetools"

# Patch goto/continue (LuaJIT feature) to Lua 5.1 compatible code
echo "Patching goto/continue for Lua 5.1 compatibility..."
python3 "$(dirname "$0")/patch-goto.py" "$BUILD_DIR"

# Create .love file
echo "Creating .love file..."
cd "$BUILD_DIR"
zip -r /tmp/playtime-web.love . -x "*.DS_Store" -x "*.git*" > /dev/null

# Package with love.js
echo "Packaging with love.js..."
rm -rf "$OUTPUT_DIR"
love.js /tmp/playtime-web.love "$OUTPUT_DIR" --title "Playtime: ${SCENE}" --memory 134217728

echo ""
echo "=== Done! ==="
echo "Output: $OUTPUT_DIR"
echo ""
echo "To serve locally:"
echo "  python3 $(dirname "$0")/server.py 8080"
echo "  # Then update server.py to point to output-${SCENE}/"
echo ""
echo "Or quick serve:"
echo "  cd $OUTPUT_DIR && python3 -c \"
import http.server
class H(http.server.SimpleHTTPRequestHandler):
    def end_headers(self):
        self.send_header('Cross-Origin-Opener-Policy','same-origin')
        self.send_header('Cross-Origin-Embedder-Policy','require-corp')
        super().end_headers()
http.server.HTTPServer(('',8080),H).serve_forever()
\""
